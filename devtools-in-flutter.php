<h2>How Do you use DevTools in Flutter?</h2>

As the name suggests, DevTools is primarily a tool. In addition, it lets us know the performance of the app. And besides that, it also helps us in debugging.

All together, Flutter or Dart DevTools is very handy. However, as a developer, you must know how to use it.

First of all, to open DevTools you need to run your app first. Then you'll click the DevTools link so that it opens either in Chrome or Mozilla browser.

In Visual Studio Code Editor at the bottom right corner you will find the DevTools. Watch the next image.

