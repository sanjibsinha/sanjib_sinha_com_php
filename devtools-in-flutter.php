<h2>What is Flutter tool?</h2>
People often find it ambiguous and confuse flutter with DevTools. Certainly, both are tools all the same they are not equal in meaning.

On the contrary, Flutter is a UI software tool kit, whereas DevTools gives you support regarding the application you make with Flutter.

Now, question is what can we do with the help of DevTools?

<p></p><img class="size-large wp-image-674" src="https://sanjibsinha.com/wp-content/uploads/2021/04/1-2-1024x310.jpg" alt="Flutter DevTools showing the app structure" width="790" height="239" /><p></p> Flutter DevTools showing the app structure<p></p>

On the left side we can see the emulator. And on the right side the DevTools shows the app structure. Since we've clicked the OutLinedButton, it gives us all the details about the button.

Moreover, it inspects the whole UI layout and gives us details of the state of flutter app.
